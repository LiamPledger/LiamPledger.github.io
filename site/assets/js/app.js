function setupMobileMenu() {
  const sidebar = document.getElementById("sidebar");
  const button = document.getElementById("mobile-menu-btn");
  if (!sidebar || !button) return;

  button.addEventListener("click", () => {
    sidebar.classList.toggle("open");
  });

  sidebar.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      sidebar.classList.remove("open");
    });
  });
}

function setActiveNav() {
  const page = document.body?.dataset?.page;
  if (!page) return;

  document.querySelectorAll("[data-nav]").forEach((link) => {
    if (link.dataset.nav === page) {
      link.classList.add("active");
    }
  });
}

function updateStats() {
  const pubCountEl = document.getElementById("pub-count");
  const citationEl = document.getElementById("citation-total");
  const totalCitations = publications.reduce((acc, p) => (Number.isFinite(p.citations) ? acc + p.citations : acc), 0);

  if (pubCountEl) pubCountEl.textContent = String(publications.length);
  if (citationEl) citationEl.textContent = String(totalCitations);
}

function downloadText(filename, text) {
  const blob = new Blob([text], { type: "text/plain;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function renderPublications(filter = "all") {
  const container = document.getElementById("pub-list");
  if (!container) return;

  const visible = publications
    .filter((p) => filter === "all" || p.type === filter)
    .sort((a, b) => b.year - a.year);

  container.innerHTML = visible
    .map((p) => {
      const citationText = Number.isFinite(p.citations) ? String(p.citations) : "n/a";
      const links = p.links
        .map((l) => `<a href="${l.url}" target="_blank" rel="noopener">${l.label}</a>`)
        .join("");

      return `
        <article class="pub-item">
          <h3>${p.title}</h3>
          <p class="pub-meta">${p.authors} | ${p.venue} | ${p.year} | ${p.status} | Citations: ${citationText}</p>
          <p class="pub-apa"><strong>APA:</strong> ${p.apa}</p>
          <div class="pub-links">
            ${links}
            <button type="button" data-action="download-bib" data-id="${p.id}">Download BibTeX</button>
          </div>
        </article>
      `;
    })
    .join("");
}

function bindPublicationActions() {
  const container = document.getElementById("pub-list");
  if (!container) return;

  container.addEventListener("click", (event) => {
    const target = event.target;
    if (!(target instanceof HTMLElement)) return;
    if (target.dataset.action !== "download-bib") return;

    const pub = publications.find((p) => p.id === target.dataset.id);
    if (!pub) return;
    downloadText(`${pub.id}.bib`, pub.bibtex + "\n");
  });
}

function bindFilters() {
  const chips = document.querySelectorAll(".chip");
  if (!chips.length) return;

  chips.forEach((chip) => {
    chip.addEventListener("click", () => {
      chips.forEach((c) => c.classList.remove("active"));
      chip.classList.add("active");
      renderPublications(chip.dataset.filter || "all");
    });
  });
}

function clamp(value, min, max) {
  return Math.min(max, Math.max(min, value));
}

function buildStats(data, keys) {
  return keys.map((key) => {
    const values = data.map((row) => Number(row[key]));
    const mean = values.reduce((a, b) => a + b, 0) / values.length;
    const variance = values.reduce((acc, v) => acc + (v - mean) * (v - mean), 0) / values.length;
    return { key, mean, std: Math.sqrt(variance) || 1 };
  });
}

function knnPredict(data, stats, query, k = 14) {
  const scored = data
    .map((row) => {
      let sumSq = 0;
      for (const stat of stats) {
        const diff = (Number(query[stat.key]) - Number(row[stat.key])) / stat.std;
        sumSq += diff * diff;
      }
      return { row, distance: Math.sqrt(sumSq) };
    })
    .sort((a, b) => a.distance - b.distance)
    .slice(0, k);

  let weighted = 0;
  let wSum = 0;
  for (const n of scored) {
    const w = 1 / (n.distance + 1e-8);
    weighted += Number(n.row.drift) * w;
    wSum += w;
  }

  const prediction = weighted / wSum;
  const neighborhood = scored.map((s) => Number(s.row.drift));
  return {
    prediction,
    min: Math.min(...neighborhood),
    max: Math.max(...neighborhood)
  };
}

function createModelForm(containerId, params, onValueChange) {
  const container = document.getElementById(containerId);
  if (!container) return;

  const state = {};

  params.forEach((p) => {
    state[p.id] = p.value;

    const row = document.createElement("div");
    row.className = "model-row";

    const top = document.createElement("div");
    top.className = "model-row-top";
    const label = document.createElement("label");
    label.textContent = p.label;
    const rangeText = document.createElement("span");
    rangeText.className = "param-range";
    rangeText.textContent = `${p.min} to ${p.max}`;
    top.appendChild(label);
    top.appendChild(rangeText);

    const controls = document.createElement("div");
    controls.className = "model-inputs";

    const slider = document.createElement("input");
    slider.type = "range";
    slider.min = String(p.min);
    slider.max = String(p.max);
    slider.step = String(p.step);
    slider.value = String(p.value);

    const number = document.createElement("input");
    number.type = "number";
    number.min = String(p.min);
    number.max = String(p.max);
    number.step = String(p.step);
    number.value = String(p.value);

    const sync = (raw) => {
      const numeric = Number(raw);
      const safe = Number.isFinite(numeric) ? clamp(numeric, p.min, p.max) : p.value;
      const rounded = Number(safe.toFixed(6));
      slider.value = String(rounded);
      number.value = String(rounded);
      state[p.id] = rounded;
      onValueChange({ ...state });
    };

    slider.addEventListener("input", (e) => sync(e.target.value));
    number.addEventListener("input", (e) => sync(e.target.value));

    controls.appendChild(slider);
    controls.appendChild(number);
    row.appendChild(top);
    row.appendChild(controls);
    container.appendChild(row);
  });

  onValueChange({ ...state });
}

function setupColumnModel() {
  const formContainer = document.getElementById("column-model-form");
  const resultEl = document.getElementById("column-model-result");
  const colData = Array.isArray(window.COLUMN_MODEL_DATA) ? window.COLUMN_MODEL_DATA : [];
  if (!formContainer || !resultEl || !colData.length) return;

  const keys = ["a", "a_d", "fyt", "s_d", "fc", "v", "s_lbd", "a_s", "rhol_fyl", "rhot_fyt"];
  const stats = buildStats(colData, keys);

  createModelForm(
    "column-model-form",
    [
      { id: "a", label: "a (mm) - Shear span", value: 1000, min: 200, max: 2000, step: 50 },
      { id: "d", label: "d (mm) - Column section depth", value: 500, min: 100, max: 1000, step: 10 },
      { id: "s", label: "s (mm) - Spacing of transverse reinforcement", value: 100, min: 50, max: 500, step: 10 },
      { id: "fc", label: "f'c (MPa) - Concrete compressive strength", value: 30, min: 20, max: 90, step: 1 },
      { id: "fyl", label: "fyl (MPa) - Longitudinal steel yield strength", value: 500, min: 250, max: 550, step: 10 },
      { id: "fyt", label: "fyt (MPa) - Transverse steel yield strength", value: 500, min: 250, max: 1000, step: 10 },
      { id: "rhol", label: "rho_l (%) - Longitudinal reinforcement ratio", value: 2.0, min: 0.0, max: 5.0, step: 0.1 },
      { id: "rhot", label: "rho_t (%) - Transverse reinforcement ratio", value: 0.4, min: 0.0, max: 2.0, step: 0.05 },
      { id: "v", label: "v - Axial load ratio", value: 0.2, min: 0.0, max: 0.75, step: 0.05 },
      { id: "lbd", label: "lbd (mm) - Longitudinal bar diameter", value: 25, min: 5, max: 40, step: 1 }
    ],
    (state) => {
      const query = {
        a: state.a,
        a_d: state.a / state.d,
        fyt: state.fyt,
        s_d: state.s / state.d,
        fc: state.fc,
        v: state.v,
        s_lbd: state.s / state.lbd,
        a_s: state.a / state.s,
        rhol_fyl: state.rhol * state.fyl,
        rhot_fyt: state.rhot * state.fyt
      };

      const pred = knnPredict(colData, stats, query, 14);
      resultEl.textContent = `Estimated drift capacity: ${pred.prediction.toFixed(2)} %. Nearby data range: ${pred.min.toFixed(2)} % to ${pred.max.toFixed(2)} %.`;
    }
  );
}

